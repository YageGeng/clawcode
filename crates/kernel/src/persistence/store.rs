use std::io;
use std::path::{Path, PathBuf};

use config::SessionPersistenceConfig;
use protocol::{AgentPath, SessionId, SessionInfo};

use super::manifest::{
    SessionManifestStatus, active_manifest_record, append_manifest_record, closed_manifest_record,
    read_latest_manifest, resolve_manifest_path,
};
use super::record::{PersistedPayload, SessionMetaRecord, timestamp_now};
use super::recorder::SessionRecorder;
use super::replay::{ReplayedSession, replay_session_file};

/// File-backed session store rooted at a data-home directory.
pub(crate) struct SessionStore {
    data_home: PathBuf,
    enabled: bool,
}

/// Parameters required to create a persisted session file.
#[derive(Clone, Debug, typed_builder::TypedBuilder)]
pub(crate) struct CreateSessionParams {
    /// Session id generated by the kernel.
    pub session_id: SessionId,
    /// Agent path associated with the session.
    pub agent_path: AgentPath,
    /// Working directory used by the session.
    pub cwd: PathBuf,
    /// Provider id selected for the session.
    pub provider_id: String,
    /// Model id selected for the session.
    pub model_id: String,
    /// Rendered base prompt snapshot, if available at creation time.
    pub base_system_prompt: String,
}

impl SessionStore {
    /// Create a session store from application persistence config.
    pub(crate) fn from_config(config: &SessionPersistenceConfig) -> Self {
        let data_home = config
            .data_home
            .as_deref()
            .map(expand_tilde)
            .unwrap_or_else(default_data_home);
        Self {
            data_home,
            enabled: config.enabled,
        }
    }

    /// Create a session store using the default data-home directory.
    pub(crate) fn new_default() -> Self {
        Self {
            data_home: default_data_home(),
            enabled: true,
        }
    }

    /// Create and materialize a new session recorder with its session metadata.
    pub(crate) async fn create_session(
        &self,
        params: CreateSessionParams,
    ) -> io::Result<Option<SessionRecorder>> {
        if !self.enabled {
            return Ok(None);
        }
        let path = self.session_file_path(&params.session_id);
        let recorder = SessionRecorder::new(path.clone());
        let cwd = params.cwd;
        let meta = SessionMetaRecord::builder()
            .session_id(params.session_id.clone())
            .agent_path(params.agent_path.clone())
            .cwd(cwd.clone())
            .provider_id(params.provider_id)
            .model_id(params.model_id)
            .base_system_prompt(params.base_system_prompt)
            .created_at(timestamp_now())
            .build();
        recorder
            .append(&[PersistedPayload::SessionMeta(meta)])
            .await?;
        let manifest = active_manifest_record(
            &self.data_home,
            params.session_id,
            path,
            params.agent_path,
            cwd,
        );
        append_manifest_record(&self.data_home, &manifest)?;
        Ok(Some(recorder))
    }

    /// Load a persisted session by id and return replayed state plus an append recorder.
    pub(crate) fn load_session(
        &self,
        session_id: &SessionId,
    ) -> io::Result<Option<(ReplayedSession, SessionRecorder)>> {
        if !self.enabled {
            return Ok(None);
        }
        let manifest = read_latest_manifest(&self.data_home)?;
        let Some(record) = manifest.get(session_id) else {
            return Ok(None);
        };
        if record.status == SessionManifestStatus::Archived {
            return Ok(None);
        }
        let path = resolve_manifest_path(&self.data_home, &record.path);
        let replayed = replay_session_file(&path)?;
        Ok(Some((replayed, SessionRecorder::new(path))))
    }

    /// Mark a session closed in the manifest and flush its recorder.
    pub(crate) async fn close_session(
        &self,
        session_id: &SessionId,
        recorder: Option<&SessionRecorder>,
    ) -> io::Result<()> {
        if !self.enabled {
            return Ok(());
        }
        if let Some(recorder) = recorder {
            recorder.flush().await?;
        }
        let manifest = read_latest_manifest(&self.data_home)?;
        if let Some(record) = manifest.get(session_id) {
            append_manifest_record(&self.data_home, &closed_manifest_record(record))?;
        }
        Ok(())
    }

    /// List persisted sessions from the manifest, optionally filtering by cwd.
    pub(crate) fn list_sessions(&self, cwd: Option<&Path>) -> io::Result<Vec<SessionInfo>> {
        if !self.enabled {
            return Ok(Vec::new());
        }
        let manifest = read_latest_manifest(&self.data_home)?;
        let mut sessions = Vec::new();
        for record in manifest.values() {
            if record.status == SessionManifestStatus::Archived {
                continue;
            }
            let session_cwd = if record.cwd.as_os_str().is_empty() {
                let path = resolve_manifest_path(&self.data_home, &record.path);
                let Ok(replayed) = replay_session_file(&path) else {
                    continue;
                };
                replayed.meta.cwd
            } else {
                record.cwd.clone()
            };
            if let Some(cwd) = cwd
                && session_cwd != cwd
            {
                continue;
            }
            sessions.push(
                SessionInfo::builder()
                    .session_id(record.session_id.clone())
                    .cwd(session_cwd)
                    .updated_at(Some(record.updated_at.clone()))
                    .build(),
            );
        }
        sessions.sort_by(|left, right| left.session_id.0.cmp(&right.session_id.0));
        Ok(sessions)
    }

    /// Build the date-partitioned session JSONL path for a new session.
    fn session_file_path(&self, session_id: &SessionId) -> PathBuf {
        let (year, month, day) = current_utc_date_parts();
        let filename = format!("session-{}-{session_id}.jsonl", timestamp_now());
        self.data_home
            .join("sessions")
            .join(year)
            .join(month)
            .join(day)
            .join(filename)
    }
}

/// Return the default XDG data-home path used for session transcripts.
fn default_data_home() -> PathBuf {
    if let Some(value) = std::env::var_os("XDG_DATA_HOME") {
        return PathBuf::from(value).join("clawcode");
    }
    std::env::var_os("HOME")
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from("."))
        .join(".local")
        .join("share")
        .join("clawcode")
}

/// Expand a leading tilde in user-provided data-home config.
fn expand_tilde(value: &str) -> PathBuf {
    if let Some(rest) = value.strip_prefix("~/")
        && let Some(home) = std::env::var_os("HOME")
    {
        return PathBuf::from(home).join(rest);
    }
    PathBuf::from(value)
}

/// Return UTC-ish date path components derived from the process timestamp.
fn current_utc_date_parts() -> (String, String, String) {
    // Use chrono-free system time so persistence does not require additional time crate features.
    let secs = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_secs();
    let days = secs / 86_400;
    civil_from_days(days as i64)
}

/// Convert days since Unix epoch to Gregorian date components.
fn civil_from_days(days_since_epoch: i64) -> (String, String, String) {
    // Howard Hinnant's civil-from-days algorithm keeps date partitioning deterministic.
    let z = days_since_epoch + 719_468;
    let era = if z >= 0 { z } else { z - 146_096 } / 146_097;
    let doe = z - era * 146_097;
    let yoe = (doe - doe / 1_460 + doe / 36_524 - doe / 146_096) / 365;
    let mut year = yoe + era * 400;
    let doy = doe - (365 * yoe + yoe / 4 - yoe / 100);
    let mp = (5 * doy + 2) / 153;
    let day = doy - (153 * mp + 2) / 5 + 1;
    let month = mp + if mp < 10 { 3 } else { -9 };
    if month <= 2 {
        year += 1;
    }
    (
        format!("{year:04}"),
        format!("{month:02}"),
        format!("{day:02}"),
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn civil_from_days_returns_unix_epoch_date() {
        let parts = civil_from_days(0);
        assert_eq!(
            parts,
            ("1970".to_string(), "01".to_string(), "01".to_string())
        );
    }
}

#[cfg(test)]
mod store_tests {
    use super::*;
    use crate::persistence::record::{MessageRecord, PersistedPayload};
    use protocol::message::Message;

    #[tokio::test]
    async fn create_load_and_list_session_roundtrips_messages() {
        let temp = tempfile::tempdir().expect("tempdir");
        let store = SessionStore {
            data_home: temp.path().to_path_buf(),
            enabled: true,
        };
        let session_id = SessionId("session-1".to_string());
        let recorder = store
            .create_session(
                CreateSessionParams::builder()
                    .session_id(session_id.clone())
                    .agent_path(AgentPath::root())
                    .cwd(PathBuf::from("/tmp/project"))
                    .provider_id("provider".to_string())
                    .model_id("model".to_string())
                    .base_system_prompt("base".to_string())
                    .build(),
            )
            .await
            .expect("create session")
            .expect("recorder");
        recorder
            .append(&[PersistedPayload::Message(
                MessageRecord::builder()
                    .turn_id("turn-1".to_string())
                    .message(Message::user("hello"))
                    .build(),
            )])
            .await
            .expect("append message");

        let (replayed, _) = store
            .load_session(&session_id)
            .expect("load result")
            .expect("loaded session");
        assert_eq!(replayed.messages, vec![Message::user("hello")]);

        let listed = store.list_sessions(None).expect("list sessions");
        assert_eq!(listed.len(), 1);
    }

    #[tokio::test]
    async fn close_session_keeps_session_available_for_resume() {
        let temp = tempfile::tempdir().expect("tempdir");
        let store = SessionStore {
            data_home: temp.path().to_path_buf(),
            enabled: true,
        };
        let session_id = SessionId("session-closed".to_string());
        let recorder = store
            .create_session(
                CreateSessionParams::builder()
                    .session_id(session_id.clone())
                    .agent_path(AgentPath::root())
                    .cwd(PathBuf::from("/tmp/project"))
                    .provider_id("provider".to_string())
                    .model_id("model".to_string())
                    .base_system_prompt(String::new())
                    .build(),
            )
            .await
            .expect("create session")
            .expect("recorder");

        store
            .close_session(&session_id, Some(&recorder))
            .await
            .expect("close session");

        let listed = store.list_sessions(None).expect("list sessions");
        assert_eq!(listed.len(), 1);

        let loaded = store.load_session(&session_id).expect("load result");
        assert!(loaded.is_some());
    }
}
