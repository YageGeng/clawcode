/// Immutable snapshot of all context needed to execute a single turn.
#[derive(Clone, typed_builder::TypedBuilder)]
pub(crate) struct TurnContext {
    /// The session this turn belongs to.
    pub session_id: SessionId,
    /// Stable id for persistence records generated by this turn.
    pub turn_id: TurnId,
    /// Durable turn source classification.
    pub turn_kind: TurnKindRecord,
    /// Shared LLM handle for streaming completion requests.
    pub llm: ArcLlm,
    /// Shared tool registry for resolving and executing tool calls.
    pub tools: Arc<ToolRegistry>,
    /// Working directory for the session.
    pub cwd: PathBuf,
    /// Provider id used by this turn.
    pub provider_id: String,
    /// Path of the agent executing this turn.
    #[builder(default = AgentPath::root())]
    pub agent_path: AgentPath,
    /// Approval policy — controls whether tools require user confirmation.
    #[builder(default)]
    pub approval: Arc<ApprovalPolicy>,
    /// Pending approval channels. execute_turn inserts a oneshot sender;
    /// the session background task resolves it when the user responds.
    #[builder(default)]
    pub pending_approvals: Arc<
        tokio::sync::Mutex<HashMap<String, oneshot::Sender<ReviewDecision>>>,
    >,
    /// ① Agent-specific system prompt. `None` means use the default.
    #[builder(default)]
    pub agent_prompt: Option<String>,
    /// ③ Temporary user-provided system prompt. Lowest priority.
    #[builder(default)]
    pub user_system_prompt: Option<String>,
    /// Application configuration.
    #[builder(default)]
    pub app_config: Arc<AppConfig>,
    /// Skill registry for this turn's working directory.
    pub skill_registry: Arc<SkillRegistry>,
    /// Recorder for canonical turn history.
    pub recorder: Arc<dyn SessionRecorder>,
    /// Session-scoped inter-agent input queue drained between LLM requests.
    pub input_queue: Arc<tokio::sync::Mutex<InputQueue>>,
}

impl TurnContext {
    /// Persist the durable turn context snapshot for replay and audit.
    async fn persist_turn_context(&self, rendered_preamble: String) {
        let record = TurnContextRecord::builder()
            .turn_id(String::from(&self.turn_id))
            .kind(self.turn_kind.clone())
            .cwd(self.cwd.clone())
            .provider_id(self.provider_id.clone())
            .model_id(self.llm.model_id().to_string())
            .rendered_preamble(rendered_preamble)
            .build();
        if let Err(error) = self
            .recorder
            .append(&[PersistedPayload::TurnContext(record)])
            .await
        {
            tracing::warn!(%error, "failed to persist turn context");
        }
    }

    /// Persist one canonical message after it has been accepted into context history.
    async fn persist_message(&self, message: Message, usage: Option<Usage>) {
        let record = if let Some(usage) = usage {
            MessageRecord::builder()
                .turn_id(String::from(&self.turn_id))
                .message(message)
                .usage(usage)
                .build()
        } else {
            MessageRecord::builder()
                .turn_id(String::from(&self.turn_id))
                .message(message)
                .build()
        };
        if let Err(error) = self
            .recorder
            .append(&[PersistedPayload::Message(record)])
            .await
        {
            tracing::warn!(%error, "failed to persist conversation message");
        }
    }

    /// Build a turn context from session state for a prompt or inter-agent turn.
    pub(crate) fn from_session(
        rt: &Session,
        turn_id: TurnId,
        turn_kind: TurnKindRecord,
        user_system_prompt: Option<String>,
    ) -> Self {
        Self::builder()
            .session_id(rt.session_id.clone())
            .turn_id(turn_id)
            .turn_kind(turn_kind)
            .llm(Arc::clone(&rt.llm))
            .tools(Arc::clone(&rt.tools))
            .cwd(rt.cwd.clone())
            .provider_id(rt.llm.provider_id().to_string())
            .agent_prompt(rt.agent_prompt.clone())
            .pending_approvals(Arc::clone(&rt.pending_approvals))
            .agent_path(rt.agent_path.clone())
            .approval(Arc::clone(&rt.approval))
            .user_system_prompt(user_system_prompt)
            .app_config(Arc::clone(&rt.app_config))
            .skill_registry(Arc::clone(&rt.skill_registry))
            .recorder(Arc::clone(&rt.recorder))
            .input_queue(Arc::clone(&rt.input_queue))
            .build()
    }

    /// Return the configured context window for this turn's current model.
    fn model_context_tokens(&self) -> Option<u64> {
        self.app_config
            .providers
            .iter()
            .find(|provider| provider.id.as_str() == self.provider_id)
            .and_then(|provider| {
                provider
                    .models
                    .iter()
                    .find(|model| model.id == self.llm.model_id())
            })
            .and_then(|model| model.context_tokens)
    }

    /// Estimate request tokens from live history plus the rendered system preamble.
    fn estimated_request_tokens(
        &self,
        context: &dyn ContextManager,
        preamble: &str,
    ) -> usize {
        context.token_count().saturating_add(preamble.len() / 4)
    }

    /// Build the ACP-facing context-window usage snapshot for this request.
    fn context_window_usage(
        &self,
        context: &dyn ContextManager,
        preamble: &str,
    ) -> ContextWindowUsage {
        ContextWindowUsage::new(
            Self::usize_to_u64(
                self.estimated_request_tokens(context, preamble),
            ),
            self.model_context_tokens().unwrap_or(0),
        )
    }

    /// Convert local token estimates into protocol-safe counters.
    fn usize_to_u64(value: usize) -> u64 {
        u64::try_from(value).unwrap_or(u64::MAX)
    }

    /// Persist an automatic compaction checkpoint before replacing live history.
    async fn persist_compaction_checkpoint(
        &self,
        output: &CompactionOutput,
    ) -> anyhow::Result<()> {
        let record = CompactionRecord::builder()
            .turn_id(String::from(&self.turn_id))
            .summary(output.summary.clone())
            .replacement_history(output.replacement_history.clone())
            .retained_message_count(output.retained_message_count)
            .build();
        self.recorder
            .append(&[PersistedPayload::Compaction(record)])
            .await
            .map_err(anyhow::Error::from)
    }

    /// Automatically compact context when the current request estimate exceeds policy.
    async fn auto_compact_if_needed(
        &self,
        context: &mut Box<dyn ContextManager>,
        preamble: &str,
    ) -> AutoCompactionOutcome {
        let Some(policy) = AutoCompactionPolicy::from_turn_context(self) else {
            return AutoCompactionOutcome::Continue;
        };

        let before_tokens = self.estimated_request_tokens(&**context, preamble);
        if !policy.should_compact(before_tokens) {
            return AutoCompactionOutcome::Continue;
        }

        let options = CompactionOptions {
            retained_turns: self.app_config.compaction.retained_turns,
        };
        match context.compact(self.llm.as_ref(), options).await {
            Ok(Some(output)) => {
                if let Err(error) =
                    self.persist_compaction_checkpoint(&output).await
                {
                    tracing::warn!(
                        %error,
                        "automatic context compaction checkpoint persist failed"
                    );
                    return AutoCompactionOutcome::SuspendForTurn;
                }

                context.replace(output.replacement_history);
                let after_tokens =
                    self.estimated_request_tokens(&**context, preamble);
                if policy.should_compact(after_tokens) {
                    tracing::warn!(
                        before_tokens,
                        after_tokens,
                        "automatic context compaction left request above threshold"
                    );
                    AutoCompactionOutcome::SuspendForTurn
                } else {
                    AutoCompactionOutcome::Continue
                }
            }
            Ok(None) => {
                tracing::warn!(
                    before_tokens,
                    "automatic context compaction skipped because there is no summarizable history"
                );
                AutoCompactionOutcome::SuspendForTurn
            }
            Err(error) => {
                tracing::warn!(
                    before_tokens,
                    %error,
                    "automatic context compaction failed; continuing turn"
                );
                AutoCompactionOutcome::SuspendForTurn
            }
        }
    }
}

/// Automatic compaction policy derived from model metadata and user config.
#[derive(Debug, Clone, Copy)]
struct AutoCompactionPolicy {
    /// Model context window in tokens.
    context_tokens: u64,
    /// Fraction of the context window that triggers compaction.
    trigger_ratio: f64,
}

impl AutoCompactionPolicy {
    /// Build a policy for the turn, returning `None` when auto compaction is disabled.
    fn from_turn_context(ctx: &TurnContext) -> Option<Self> {
        let cfg = &ctx.app_config.compaction;
        if !cfg.auto {
            return None;
        }
        if !Self::is_valid_ratio(cfg.trigger_ratio) {
            tracing::warn!(
                trigger_ratio = cfg.trigger_ratio,
                "automatic context compaction trigger ratio is invalid"
            );
            return None;
        }

        let context_tokens = ctx.model_context_tokens()?;
        if context_tokens == 0 {
            return None;
        }

        Some(Self {
            context_tokens,
            trigger_ratio: cfg.trigger_ratio,
        })
    }

    /// Return true when the configured ratio can be applied safely.
    fn is_valid_ratio(trigger_ratio: f64) -> bool {
        trigger_ratio.is_finite() && trigger_ratio > 0.0 && trigger_ratio <= 1.0
    }

    /// Return whether estimated tokens are at or above the configured threshold.
    fn should_compact(&self, estimated_tokens: usize) -> bool {
        (estimated_tokens as f64)
            >= (self.context_tokens as f64 * self.trigger_ratio).ceil()
    }
}

/// Result of one automatic compaction check inside a turn.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum AutoCompactionOutcome {
    /// Continue normal request execution and allow future checks in this turn.
    Continue,
    /// Continue normal request execution but skip future automatic checks this turn.
    SuspendForTurn,
}

/// Execute a single turn with multi-turn tool loop support:
/// - Push the user message to context
/// - Loop: call LLM → collect response → execute any tool calls → feed results back
/// - Exit when the LLM produces a response with no tool calls
pub(crate) async fn execute_turn(
    ctx: &TurnContext,
    user_text: String,
    context: &mut Box<dyn ContextManager>,
    tx_event: &mpsc::UnboundedSender<Event>,
) -> Result<Usage, KernelError> {
    // Build the system prompt preamble once per turn.
    // The preamble is injected into every LLM request via CompletionRequest::preamble
    // and converted to a leading Message::System at build() time.
    let env_info = EnvironmentInfo::capture(
        ctx.llm.model_id().to_string(),
        ctx.cwd.clone(),
    );
    let instructions = Instructions::load(&ctx.cwd);

    // Render skill catalog for system prompt injection.
    let skills_xml = if ctx.app_config.skills.include_instructions {
        ctx.skill_registry.render_catalog()
    } else {
        None
    };

    let system_prompt = SystemPrompt::builder()
        .environment(env_info)
        .agent_prompt(ctx.agent_prompt.clone())
        .instructions(instructions)
        .skills_xml(skills_xml)
        .user_prompt(ctx.user_system_prompt.clone())
        .build();

    let preamble = system_prompt.render();
    // Persist the turn snapshot before the first message so replay can explain
    // which dynamic prompt inputs produced the following canonical history.
    ctx.persist_turn_context(preamble.clone()).await;

    let user_message = Message::user(user_text);
    context.push(user_message.clone());
    // Persist only after the message is accepted into context so the file mirrors replay order.
    ctx.persist_message(user_message, None).await;

    let tool_defs = ctx.tools.definitions();
    let sid = &ctx.session_id;
    let mut turn_usage = Usage::default();
    let mut auto_compaction_suspended = false;

    let tool_ctx = ToolContext::builder()
        .session_id(ctx.session_id.clone())
        .cwd(ctx.cwd.clone())
        .agent_path(ctx.agent_path.clone())
        .approval_mode(ctx.approval.mode())
        .build();

    loop {
        drain_inter_agent_inputs_for_turn(ctx, &mut **context).await;
        if !auto_compaction_suspended
            && ctx.auto_compact_if_needed(context, &preamble).await
                == AutoCompactionOutcome::SuspendForTurn
        {
            auto_compaction_suspended = true;
        }
        let history = context.history().to_vec();
        let history = OneOrMany::many(history)
            .map_err(|e| KernelError::Internal(e.into()))?;
        let request_context_window =
            ctx.context_window_usage(&**context, &preamble);

        // Preserve already-observed provider usage while refreshing context counters
        // between tool-loop model requests.
        let request_usage =
            (turn_usage != Usage::default()).then_some(turn_usage);
        let _ = tx_event.send(Event::usage_update(
            sid.clone(),
            request_context_window,
            request_usage,
        ));

        let request = CompletionRequest::builder()
            .model(Some(ctx.llm.model_id().to_string()))
            .preamble(Some(preamble.clone()))
            .chat_history(history)
            .tools(tool_defs.clone())
            .build();

        let mut stream = ctx.llm.stream(request).await.map_err(|e| {
            KernelError::Internal(anyhow::anyhow!("LLM stream error: {e}"))
        })?;

        let mut assistant_content: Vec<AssistantContent> = Vec::new();
        let mut tool_outputs: Vec<ToolOutput> = Vec::new();
        let mut streamed_reasoning_preview = String::new();
        let mut argument_consumers = ArgumentsConsumers::default();
        let mut response_usage = None;

        // Track whether the assistant content has final text that can complete the turn
        let mut is_answer = false;
        // Track whether the provider has signalled end-of-stream via Final.
        // The turn must not be considered complete until Final is observed,
        // even if is_answer is true and no tool calls were made.
        let mut final_received = false;

        while let Some(event) = stream.next().await {
            let event = event.map_err(|e| {
                KernelError::Internal(anyhow::anyhow!(
                    "Stream event error: {e}"
                ))
            })?;

            match event {
                LlmStreamEvent::Text(text) => {
                    assistant_content.push(AssistantContent::text(&text.text));
                    let _ = tx_event
                        .send(Event::message_chunk(sid.clone(), text.text));

                    // Mark the answer as complete when we receive text content
                    is_answer = true;
                }
                LlmStreamEvent::ToolCall {
                    mut tool_call,
                    internal_call_id,
                } => {
                    // If the tool call ID is empty, use the internal call ID as a fallback
                    if tool_call.id.is_empty() {
                        tool_call.id = internal_call_id.clone();
                    }

                    // Always use the canonical tool-call id for frontend correlation.
                    // Keep `internal_call_id` only as a parser-local fallback.
                    let event_call_id = tool_call.id.clone();

                    // mark streaming arguments consumer is finished
                    for preview_event in
                        argument_consumers.finish(sid, event_call_id.as_str())
                    {
                        let _ = tx_event.send(preview_event);
                    }

                    let (text, _succeeded) = dispatch_tool(
                        ctx,
                        tx_event,
                        &event_call_id,
                        &tool_call,
                        &tool_ctx,
                    )
                    .await?;

                    assistant_content
                        .push(AssistantContent::ToolCall(tool_call.clone()));

                    tool_outputs.push(ToolOutput {
                        id: tool_call.id,
                        call_id: tool_call.call_id,
                        output: text,
                    });
                }
                LlmStreamEvent::Reasoning(reasoning) => {
                    let text = reasoning.display_text();
                    let emit_text = should_emit_reasoning_block(
                        &mut streamed_reasoning_preview,
                        &text,
                    );
                    append_reasoning_block(&mut assistant_content, reasoning);
                    if emit_text {
                        let _ = tx_event
                            .send(Event::thought_chunk(sid.clone(), text));
                    }
                }
                LlmStreamEvent::ReasoningDelta {
                    id,
                    reasoning,
                    replayable,
                } => {
                    // Non-replayable reasoning deltas are UI previews, such as
                    // OpenAI reasoning summaries. They are tracked only to avoid
                    // redisplaying the canonical reasoning item when it later
                    // arrives with `reasoning.encrypted_content` for stateless
                    // `store=false` replay.
                    if !replayable {
                        streamed_reasoning_preview.push_str(&reasoning);
                    }
                    append_reasoning_delta(
                        &mut assistant_content,
                        id,
                        &reasoning,
                        replayable,
                    );
                    let _ = tx_event
                        .send(Event::thought_chunk(sid.clone(), reasoning));
                }
                LlmStreamEvent::ToolCallDelta {
                    internal_call_id,
                    id,
                    content,
                    ..
                } => {
                    // Forward incremental tool call arguments to the frontend.
                    let call_id = if id.is_empty() {
                        internal_call_id.as_str()
                    } else {
                        id.as_str()
                    };

                    match &content {
                        protocol::ToolCallDeltaContent::Name(name) => {
                            argument_consumers
                                .maybe_create(call_id, name, &ctx.tools);
                        }

                        protocol::ToolCallDeltaContent::Delta(delta) => {
                            for preview_event in argument_consumers
                                .consume_delta(sid, call_id, delta)
                            {
                                let _ = tx_event.send(preview_event);
                            }
                        }
                    }

                    let _ = tx_event.send(Event::tool_call_delta(
                        sid.clone(),
                        call_id,
                        content,
                    ));
                }
                LlmStreamEvent::Final { usage, .. } => {
                    final_received = true;
                    if let Some(usage) = usage {
                        response_usage = Some(usage);
                        turn_usage += usage;
                        let _ = tx_event.send(Event::usage_update(
                            sid.clone(),
                            request_context_window,
                            Some(usage),
                        ));
                    }
                }
            }
        }

        // Save assistant message from this iteration
        if !assistant_content.is_empty() {
            let assistant_message = Message::Assistant {
                id: None,
                content: OneOrMany::many(assistant_content).unwrap_or_else(
                    |_| OneOrMany::one(AssistantContent::text("")),
                ),
            };
            context.push(assistant_message.clone());
            ctx.persist_message(assistant_message, response_usage).await;
        }

        // If no tool calls were made and the provider has signalled the
        // stream is finished, the turn is done.
        if tool_outputs.is_empty() && is_answer && final_received {
            return Ok(turn_usage);
        }

        // Feed tool outputs back as user messages for the next LLM iteration
        for output in tool_outputs {
            let tool_message = Message::User {
                content: OneOrMany::one(
                    protocol::message::UserContent::ToolResult(ToolResult {
                        id: output.id,
                        call_id: output.call_id,
                        content: OneOrMany::one(ToolResultContent::Text(
                            protocol::message::Text {
                                text: output.output,
                            },
                        )),
                    }),
                ),
            };
            context.push(tool_message.clone());
            ctx.persist_message(tool_message, None).await;
        }
    }
}

/// Drain queued inter-agent messages into the active turn before the next LLM request.
async fn drain_inter_agent_inputs_for_turn(
    ctx: &TurnContext,
    context: &mut dyn ContextManager,
) {
    let messages = ctx.input_queue.lock().await.drain_mailbox_input_items();
    for message in messages {
        let user_message = Message::user(message.render_model_context());
        context.push(user_message.clone());
        ctx.persist_message(user_message, None).await;
    }
}

/// Append a replayable reasoning delta, merging contiguous reasoning chunks.
///
/// Display-only previews are intentionally skipped because providers such as
/// OpenAI require the completed reasoning item, including encrypted content, to
/// be replayed in later stateless `store=false` requests.
fn append_reasoning_delta(
    assistant_content: &mut Vec<AssistantContent>,
    id: Option<String>,
    reasoning: &str,
    replayable: bool,
) {
    if !replayable {
        return;
    }

    if let Some(AssistantContent::Reasoning(existing)) =
        assistant_content.last_mut()
        && let Some(ReasoningContent::Text { text, .. }) =
            existing.content.last_mut()
    {
        text.push_str(reasoning);
        return;
    }

    assistant_content.push(AssistantContent::Reasoning(
        Reasoning::new(reasoning).optional_id(id),
    ));
}

/// Append a complete reasoning block, merging adjacent canonical provider items.
fn append_reasoning_block(
    assistant_content: &mut Vec<AssistantContent>,
    reasoning: Reasoning,
) {
    if reasoning.id.is_some()
        && let Some(AssistantContent::Reasoning(existing)) =
            assistant_content.last_mut()
        && existing.id == reasoning.id
    {
        existing.content.extend(reasoning.content);
        return;
    }

    assistant_content.push(AssistantContent::Reasoning(reasoning));
}

/// Decide whether a canonical reasoning block should be displayed to the UI.
fn should_emit_reasoning_block(
    streamed_preview: &mut String,
    text: &str,
) -> bool {
    if text.is_empty() {
        return false;
    }

    if streamed_preview == text {
        streamed_preview.clear();
        return false;
    }

    true
}

/// Captures a tool execution result and the provider correlation id.
struct ToolOutput {
    /// Internal call id used by clawcode event streams.
    id: String,
    /// Provider call id required by APIs that separate local and remote ids.
    call_id: Option<String>,
    /// Text result sent back to the model.
    output: String,
}

/// Manages the lifecycle of tool arguments consumers, mapping call ids to their state.
#[derive(Default)]
struct ArgumentsConsumers {
    consumers: HashMap<String, Box<dyn ToolArgumentsConsumer>>,
}

impl ArgumentsConsumers {
    /// Create a consumer for a tool call once the provider streams its tool name.
    fn maybe_create(
        &mut self,
        call_id: &str,
        tool_name: &str,
        tools: &ToolRegistry,
    ) {
        if self.consumers.contains_key(call_id) {
            return;
        }
        let Some(tool) = tools.get(tool_name) else {
            return;
        };
        let Some(consumer) = tool.arguments_consumer() else {
            return;
        };
        self.consumers.insert(call_id.to_string(), consumer);
    }

    /// Route one streamed arguments delta to the matching consumer.
    fn consume_delta(
        &mut self,
        session_id: &SessionId,
        call_id: &str,
        delta: &str,
    ) -> Vec<Event> {
        let Some(consumer) = self.consumers.get_mut(call_id) else {
            return Vec::with_capacity(0);
        };

        consumer
            .consume_delta(call_id, delta)
            .into_iter()
            .map(|item| match item {
                protocol::ToolArgumentsStreamItem::PatchPreview {
                    call_id,
                    changes,
                } => Event::patch_apply_updated(
                    session_id.clone(),
                    call_id,
                    changes,
                ),
            })
            .collect()
    }

    /// Flush the matching consumer immediately before the final tool call executes.
    fn finish(&mut self, session_id: &SessionId, call_id: &str) -> Vec<Event> {
        let Some(mut consumer) = self.consumers.remove(call_id) else {
            return Vec::new();
        };
        match consumer.finish(call_id) {
            Ok(items) => items
                .into_iter()
                .map(|item| match item {
                    protocol::ToolArgumentsStreamItem::PatchPreview {
                        call_id,
                        changes,
                    } => Event::patch_apply_updated(
                        session_id.clone(),
                        call_id,
                        changes,
                    ),
                })
                .collect(),
            Err(error) => {
                tracing::debug!(%call_id, %error, "tool arguments consumer failed to finish");
                Vec::new()
            }
        }
    }
}

/// Dispatch a tool call, driving the unified `execute_streaming` path.
///
/// Streaming tools (`supports_streaming == true`) produce `Begin`/`End`
/// lifecycle items; non-streaming tools get a synthetic `ToolCall(InProgress)`
/// before execution and a `Text` item wrapping their `execute()` result.
///
/// Returns `(output_text, succeeded)` on completion, or
/// `Err(KernelError::Cancelled)` on abort.
async fn dispatch_tool(
    ctx: &TurnContext,
    tx_event: &mpsc::UnboundedSender<Event>,
    call_id: &str,
    tool_call: &ToolCall,
    tool_ctx: &ToolContext,
) -> Result<(String, bool), KernelError> {
    use protocol::ToolCallStatus;
    let tool_name = &tool_call.function.name;
    let arguments = &tool_call.function.arguments;

    let Some(tool) = ctx.tools.get(tool_name) else {
        // Unknown tools can be hallucinated by the model or removed by config.
        // Emit a failed tool row and return a failed result so the model can choose an available tool.
        let msg = format!("unknown tool `{tool_name}`");
        let _ = tx_event.send(Event::tool_call(
            ctx.session_id.clone(),
            ctx.agent_path.clone(),
            call_id,
            tool_name,
            arguments.clone(),
            ToolCallStatus::Failed,
        ));
        let _ = tx_event.send(Event::tool_call_update(
            ctx.session_id.clone(),
            call_id,
            Some(msg.clone()),
            Some(ToolCallStatus::Failed),
        ));

        return Ok((msg, false));
    };

    let needs_approval = tool.needs_approval(arguments, tool_ctx);

    if needs_approval && ctx.approval.mode() != ApprovalMode::Yolo {
        // Approval-required tools need an explicit pending event first so the
        // frontend can show the confirm action before execution starts.
        let _ = tx_event.send(Event::tool_call(
            ctx.session_id.clone(),
            ctx.agent_path.clone(),
            call_id,
            tool_name,
            arguments.clone(),
            ToolCallStatus::Pending,
        ));
        match request_tool_approval(
            ctx,
            tx_event,
            call_id,
            tool_name,
            arguments.clone(),
        )
        .await?
        {
            ReviewDecision::Abort => return Err(KernelError::Cancelled),
            ReviewDecision::AllowOnce | ReviewDecision::AllowAlways => {}
            _ => {
                let msg = "rejected by user".to_string();
                let _ = tx_event.send(Event::tool_call_update(
                    ctx.session_id.clone(),
                    call_id,
                    Some(msg.clone()),
                    Some(ToolCallStatus::Failed),
                ));
                return Ok((msg, false));
            }
        }
        // Once approved, convert the call to InProgress so UI keeps a stable
        // tool-call anchor while the execution stream is running.
        let _ = tx_event.send(Event::tool_call_update(
            ctx.session_id.clone(),
            call_id,
            None,
            Some(ToolCallStatus::InProgress),
        ));
    } else {
        // Tools that do not require approval can begin immediately; emit an
        // InProgress ToolCall so clients that subscribe to call status can
        // render a live tool row.
        let _ = tx_event.send(Event::tool_call(
            ctx.session_id.clone(),
            ctx.agent_path.clone(),
            call_id,
            tool_name,
            arguments.clone(),
            ToolCallStatus::InProgress,
        ));
    }

    let mut stream =
        match tool.execute_streaming(arguments.clone(), tool_ctx).await {
            Ok(result) => result,
            Err(err) => {
                // Tool-level failures are model-visible results, not kernel failures.
                // This lets the model repair bad tool inputs such as non-matching patches.
                let _ = tx_event.send(Event::tool_call_update(
                    ctx.session_id.clone(),
                    call_id,
                    Some(err.clone()),
                    Some(ToolCallStatus::Failed),
                ));
                return Ok((err, false));
            }
        };

    let mut succeeded = true;
    let mut output_text = String::new();
    while let Some(item) = stream.next().await {
        match item {
            ToolStreamItem::Begin(turn_item) => {
                let _ = tx_event.send(Event::item_started(
                    ctx.session_id.clone(),
                    ctx.turn_id.clone(),
                    turn_item,
                ));
            }
            ToolStreamItem::End(turn_item) => {
                succeeded = match &turn_item {
                    TurnItem::ExecCommand(item) => {
                        matches!(item.status, ExecCommandStatus::Completed)
                    }
                    TurnItem::FileChange(item) => {
                        matches!(item.status, FileChangeStatus::Completed)
                    }
                    TurnItem::McpToolCall(item) => {
                        matches!(item.status, McpToolCallStatus::Completed)
                    }
                };
                // Emit a final status update before item completion so ACP clients
                // can show a deterministic transition to completed/failed.
                let status = if succeeded {
                    ToolCallStatus::Completed
                } else {
                    ToolCallStatus::Failed
                };
                let _ = tx_event.send(Event::tool_call_update(
                    ctx.session_id.clone(),
                    call_id,
                    None,
                    Some(status),
                ));
                let _ = tx_event.send(Event::item_completed(
                    ctx.session_id.clone(),
                    ctx.turn_id.clone(),
                    turn_item,
                ));
            }
            ToolStreamItem::Delta { stream, chunk } => {
                let _ = tx_event.send(Event::ExecCommandOutputDelta {
                    session_id: ctx.session_id.clone(),
                    call_id: call_id.to_string(),
                    stream,
                    chunk,
                });
            }
            ToolStreamItem::Final { content, is_error } => {
                succeeded = !is_error;
                // Streaming tools can only know the final model-facing result
                // after their live output loop completes.
                output_text = content.clone();
                let _ = tx_event.send(Event::tool_call_update(
                    ctx.session_id.clone(),
                    call_id,
                    Some(content),
                    Some(if is_error {
                        ToolCallStatus::Failed
                    } else {
                        ToolCallStatus::Completed
                    }),
                ));
            }
        }
    }

    Ok((output_text, succeeded))
}

/// Request approval for a tool call and return the frontend decision.
/// Blocks on a oneshot channel until the session background task
/// receives the user's response.
async fn request_tool_approval(
    ctx: &TurnContext,
    tx_event: &mpsc::UnboundedSender<Event>,
    call_id: &str,
    tool_name: &str,
    arguments: serde_json::Value,
) -> Result<ReviewDecision, KernelError> {
    let (tx_approve, rx_approve) = oneshot::channel();
    {
        let mut approvals = ctx.pending_approvals.lock().await;
        approvals.insert(call_id.to_string(), tx_approve);
    }

    let event = Event::exec_approval(
        ctx.session_id.clone(),
        call_id,
        tool_name,
        arguments,
        &ctx.cwd,
    );

    if tx_event.send(event).is_err() {
        ctx.pending_approvals.lock().await.remove(call_id);
        return Err(KernelError::Internal(anyhow::anyhow!(
            "failed to deliver approval request for tool call {call_id}"
        )));
    }

    rx_approve.await.map_err(|_e| {
        KernelError::Internal(anyhow::anyhow!(
            "approval channel closed for tool call {call_id}"
        ))
    })
}

